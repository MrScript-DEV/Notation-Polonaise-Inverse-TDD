class NotationPolonaiseInverse:
    def addition(self, a, b):
        return a + b

    def soustraction(self, a, b):
        return a - b

    def multiplication(self, a, b):
        return a * b

    def division(self, a, b):
        return a / b

    def calcul(self, a, b, operateur):
        return self.addition(a, b)
